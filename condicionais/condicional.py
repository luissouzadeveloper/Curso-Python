# Condicionais

# if
idade = 18
if idade >= 18:
    print('É de maior!')

# if-else
idade_2 = 15
if idade_2 >= 18:
    print('Pode tirar a habilitação')
else:
    print('Ainda não pode tirar a habilitação')

# if-elif
nota = 6.4

if nota >= 9.0:
    print('Nota excelente')
elif nota >= 8.9:
    print('Nota regular')
elif nota >= 7.0:
    print('Nota baixa')
else:
    print('Reprovado!')